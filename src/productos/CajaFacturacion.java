    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        int selectedRow = tablaProductos.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione una fila para modificar", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String cantidad = modelo.getValueAt(selectedRow, 0).toString();
        String nombreProducto = modelo.getValueAt(selectedRow, 1).toString();
        String precio = modelo.getValueAt(selectedRow, 2).toString();

        JTextField txtCantidad = new JTextField(cantidad);
        JTextField txtNombreProducto = new JTextField(nombreProducto);
        JTextField txtPrecio = new JTextField(precio);

        Object[] message = {
            "Cantidad:", txtCantidad,
            "Nombre Producto:", txtNombreProducto,
            "Precio:", txtPrecio
        };

        int option = JOptionPane.showConfirmDialog(null, message, "Modificar Producto", JOptionPane.OK_CANCEL_OPTION);
        if (option == JOptionPane.OK_OPTION) {
            try {
                int nuevaCantidad = Integer.parseInt(txtCantidad.getText());
                double nuevoPrecio = Double.parseDouble(txtPrecio.getText());
                double nuevoTotal = nuevaCantidad * nuevoPrecio;

                modelo.setValueAt(nuevaCantidad, selectedRow, 0);
                modelo.setValueAt(txtNombreProducto.getText(), selectedRow, 1);
                modelo.setValueAt(nuevoPrecio, selectedRow, 2);
                modelo.setValueAt(nuevoTotal, selectedRow, 3);

                calcularTotales();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Por favor, ingrese valores v谩lidos.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    
    }//GEN-LAST:event_btnModificarActionPerformed

